#include "libft.h"

int		ft_str_is_lowercase(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if (ft_islower(str[i]) == 0)
			return (0);
		i++;
	}
	return (1);
}
