#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int		i;
	int		c;

	i = ft_strlen(dest);
	c = 0;
	while (src[c] != '\0')
	{
		dest[i + c] = src[c];
		c++;
	}
	dest[i + c] = '\0';
	return (dest);
}
