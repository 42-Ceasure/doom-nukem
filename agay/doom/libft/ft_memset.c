#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	int		i;
	int		size;

	i = 0;
	size = len;
	while (i < size)
	{
		((char *)b)[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
