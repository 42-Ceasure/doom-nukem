#include "libft.h"

char	*ft_strlowcase(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (ft_isupper(str[i]) == 1)
			str[i] = str[i] + ' ';
		i++;
	}
	return (str);
}
